package adaptation;

public class Eyes {
	private int visionRating=0;
	private int nightVision=0;

	public Eyes() {}

	public void setVisionRating(int v) {
		visionRating=v;
	}

	public int getVisionRating() {
		return visionRating;
	}

	public void setNightVision(int n) {
		nightVision=n;
	}

	public int getNightVision() {
		return nightVision;
	}
}